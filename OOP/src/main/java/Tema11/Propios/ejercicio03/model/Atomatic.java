package Tema11.Propios.ejercicio03.model;

import java.util.ArrayList;

public class Atomatic extends Car{

   
    public Atomatic(String licensePlate, ArrayList<Integer> arrayList) {
        super(licensePlate, arrayList);
    }

    @Override
    public double breakSpeed(double value) {
        return super.breakSpeed(value);
    }

    @Override
    public double speedUp(double value) {
        return super.speedUp(value);
    }

   
  
    
}
